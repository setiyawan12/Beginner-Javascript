//Boolean
//Tipe data boolean merepresentasikan entitas logika. 
//‘boolean’ dapat merepresentasikan nilai benar atau salah, satu atau nol.

var benar = true; //Boolean True <Benar>
var salah = false; //Boolean False <Salah>
console.log(benar);
console.log(salah);

//null
//Tipe data ‘null’ merepresentasikan ‘tidak memiliki nilai apapun’. 
///Variabel yang memiliki nilai null menandakan bahwa variabel tersebut tidak menunjuk pada object/nilai apapun
var tidakPunyaNilai = null
console.log(tidakPunyaNilai);