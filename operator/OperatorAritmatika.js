//Dengan Metode Variable//
var nilai1 = 10;
var nilai2 = 5;

//===========================
//Penjumlahan, Pada JavaScript, operator penjumlahan menggunakan karakter plus, +.
console.log('====Tambah====');
console.log(nilai1+nilai2); //Variable
console.log(2+nilai1+nilai2); //Expression
//============================
//Pengurangan,Operator pengurangan menggunakan karakter minus, -.
console.log('====Kurang====');
console.log(nilai1-nilai2); //Variable
console.log(2-nilai1-nilai2); //Expression
//============================
//Perkalian, Operator perkalian menggunakan karakter bintang, *.
console.log('====Perkalian====');
console.log(nilai1*nilai2); //Variable
console.log(2*nilai1*nilai2); //Expression
//============================
//Pembagian, Operasi pembagian menggunakan karakter slash, /.
console.log('====Pembagian====');
console.log(nilai1/nilai2); //Variable
console.log(2/nilai1/nilai2); //Expression

//End//

